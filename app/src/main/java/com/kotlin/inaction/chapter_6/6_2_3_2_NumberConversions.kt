fun main(args: Array<String>) {
    val l: Long = 123L
    println(l)
    val d: Double = 0.123
    println(d)
    val d2: Double = 123.0
    println(d2)
    val d3: Double = 1.23e10
    println(d3)
    val d4: Double = 1.23e-10
    println(d4)
    val f: Float = 123.4f
    println(f)
    val f1: Float = .456F
    println(f1)
    val f2: Float = 1e3f
    println(f2)
    val hex1: Int = 0xABCDE
    println(hex1)
    val hex2: Int = 0xabcde
    println(hex2)
    val b: Int = 0b110
    println(b)
    val b2: Int = 0B001
    println(b2)

}