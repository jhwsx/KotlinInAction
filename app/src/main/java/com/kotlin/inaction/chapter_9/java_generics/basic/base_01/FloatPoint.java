package com.kotlin.inaction.chapter_9.java_generics.basic.base_01;

/**
 * Float 类型的 Point
 *
 * @author wangzhichao
 * @date 2019/11/01
 */
public class FloatPoint {
    private Float x;
    private Float y;

    public Float getX() {
        return x;
    }

    public void setX(Float x) {
        this.x = x;
    }

    public Float getY() {
        return y;
    }

    public void setY(Float y) {
        this.y = y;
    }
}
