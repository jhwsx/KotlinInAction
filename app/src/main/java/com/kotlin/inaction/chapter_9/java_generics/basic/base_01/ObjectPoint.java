package com.kotlin.inaction.chapter_9.java_generics.basic.base_01;

/**
 * Object 类型的 Point
 * @author wangzhichao
 * @date 2019/11/01
 */
public class ObjectPoint {
    private Object x;
    private Object y;

    public Object getX() {
        return x;
    }

    public void setX(Object x) {
        this.x = x;
    }

    public Object getY() {
        return y;
    }

    public void setY(Object y) {
        this.y = y;
    }
}
