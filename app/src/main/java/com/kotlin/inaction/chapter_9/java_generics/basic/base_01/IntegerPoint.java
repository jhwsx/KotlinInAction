package com.kotlin.inaction.chapter_9.java_generics.basic.base_01;

/**
 * Integer 类型的 Point
 * @author wangzhichao
 * @date 2019/11/01
 */
public class IntegerPoint {
    private Integer x;
    private Integer y;

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
